package com.class11;

public class MultDimArrayStrings {

	public static void main(String[] args) {
		// String 2D array
		
		String[][] groups= {
				{"Jawid","Mustafa","Amina","Mahri","Emily","Ahmadi"},
				{"Bilal","Shaban","Asmit","Sasha","Olga","Moe"},
		};
		
		System.out.println(groups[0][1]);
		System.out.println(groups[1][1]+" "+groups[1][2]);
				

	}

}
