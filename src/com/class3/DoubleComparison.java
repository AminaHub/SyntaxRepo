package com.class3;

public class DoubleComparison {

	public static void main(String[] args) {
		double a=23.34;
		double b=15.98;
		
		if (a>b) {
			System.out.println("Double value of "+a+" is larger than "+b);
		}else {
			System.out.println("Double value of "+b+ " is larger than "+a); 
		}
		
		double c=23.34;
		double d=15.98;
		
		if (c<d) {
			System.out.println("Double value of "+c+" is larger than "+d);
		}else {
			System.out.println("Double value of "+d+ " is less than "+c); 
		}
	}

}
