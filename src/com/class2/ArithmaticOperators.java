package com.class2;

public class ArithmaticOperators {
	public static void main (String[] args) {
		//declear 2 int variables and then we perform:
		//addition, subtraction, multiplication, division
		
		int a=30;
		int b=15;
		
		System.out.println(a+b); //45
		System.out.println(a-b); //15
		System.out.println(a*b); //450
		System.out.println(a/b); //2
		
		
		int sum=a+b;
		int sub=a-b;
		int mult=a*b;
		int div=a/b;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);

		System.out.println("the sum of 2 numbers is " +sum);
		System.out.println("the sub of 2 numbers is " +sub);
		System.out.println("the product of 2 numbers is " +mult);
		System.out.println("the division of 2 numbers is " +div);
		
		double m=30.50;
		double n=20.55;
		
		System.out.println(m+n); 
		System.out.println(m-n); 
		System.out.println(m*n); 
		System.out.println(m/n); 	
		
		
		double sum1=m+n;
		double sub1=m-n;
		double mult1=m*n;
		double div1=m/n;
		
		System.out.println(sum1);
		System.out.println(sub1);
		System.out.println(mult1);
		System.out.println(div1);

		System.out.println("the addition of 2 double numbers is " +sum1);
		System.out.println("the sub of 2 double numbers is " +sub1);
		System.out.println("the product of 2 double numbers is " +mult1);
		System.out.println("the division of 2 double numbers is " +div1);
		

}
}