package com.class2;

public class RectangleArea {
	public static void main (String[] args) {
		
		//Area of a rectangle is width * length
		
		int h=8;
		int w=5;
		
		int area=h*w;
		
		//Perimeter of a rectangle P=2(l+w)
		int per=2*(h+w);
		
		System.out.println("Area of a rectangular is "+area);
		System.out.println("Peremiter of a rectangle is "+per);
		//The perimeter of a rectangle with width____ of and hight____ of is ____
		System.out.println("The perimeter of a rectangle with width of "+w+" and hight of "+h+" is "+per+".");
				
		
	}

}
